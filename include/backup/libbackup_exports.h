/*
    File Description:

    The header file that defines the export symbols for the BACKUP library
 */

#include <common/library_indicators.h>

#ifdef EXPORTLIBRARY
#define EXPORTLIBRARY something else
#error ExportLibrary was already defined
#endif

#define EXPORTLIBRARY            libbackup

#if !defined(IPLIB)
#   error IPLIB is not defined
#endif

/* Handwritten code should use BACKUP_API, not BACKUPEXPORT */

#define BACKUP_API BACKUPEXPORT

/* Support BACKUPEXPORT for autogenerated schema/pif code only */

#if IPLIB==libbackup
#   if defined(__lint)
#       define BACKUPEXPORT       __export(backup)
#       define BACKUPGLOBAL       extern __global(backup)
#       define BACKUPPRIVATE      extern __private(backup)
#   elif defined(_WIN32)
#       define BACKUPEXPORT       __declspec(dllexport)
#       define BACKUPGLOBAL       extern __declspec(dllexport)
#       define BACKUPPRIVATE      extern
#   else
#       define BACKUPEXPORT
#       define BACKUPGLOBAL       extern
#       define BACKUPPRIVATE      extern
#   endif
#else
#   if defined(__lint)
#       define BACKUPEXPORT       __export(backup)
#       define BACKUPGLOBAL       extern __global(backup)
#   elif defined(_WIN32) && !defined(WNT_STATIC_LINK)
#       define BACKUPEXPORT      __declspec(dllimport)
#       define BACKUPGLOBAL       extern __declspec(dllimport)
#   else
#       define BACKUPEXPORT
#       define BACKUPGLOBAL       extern
#   endif
#endif

